package com.lit.day2;
// for loop is same as while loop the difference is the condition in while loop is given in three lines where in forloop 
//everything is in single line// there by reduce lines and memory
public class forloopDemo {

	public static void main(String[] args) {
		
	   for(int i=1; i<=7;i++)
	   {
			  System.out.println("value of i is:");
			  System.out.println(i);
	   }
				
	}

}
